            if (Math.abs(blocks.get(i).x - x) <= 5 && Math.abs(blocks.get(i).y - y) <= 5 && blocks.get(i).isAlive) {
                playerCollesionDirection = playerDirection;
                blockColletion = 1;
                collapsedBlockIndex = i;
            }
